#!/usr/bin/env node
// Concatenate SKILL.md + references/* into a single universal markdown bundle
// for non-Claude LLMs (ChatGPT, Perplexity, DeepSeek, Cursor without MCP).
//
// Usage:  node scripts/build-llm-context.mjs
// Writes: data-skill/llm-context.md

import { readFileSync, writeFileSync, readdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = join(__dirname, '..')
const skillDir = join(repoRoot, 'data-skill')
const referencesDir = join(skillDir, 'references')
const outputPath = join(skillDir, 'llm-context.md')

const orderedReferences = [
  'endpoints.md',
  'units-and-gotchas.md',
  'entities.md',
  'cookbook.md',
  'rpc-fallback.md',
]

const stripFrontmatter = (content) => {
  if (!content.startsWith('---\n')) return content
  const closing = content.indexOf('\n---\n', 4)
  if (closing === -1) return content
  return content.slice(closing + 5).trimStart()
}

const header = `# StakeWise data-query — universal LLM context

This file is a concatenation of the \`stakewise-data-query\` skill (SKILL.md + all references) intended for non-Claude LLMs. Claude Code users get this content automatically via plugin install; everyone else can paste the raw URL of this file into a system prompt, Perplexity Space, or Cursor \`@-mention\`.

**Raw URL:** \`https://raw.githubusercontent.com/stakewise/llm-tools/main/data-skill/llm-context.md\`

**Source repo:** https://github.com/stakewise/llm-tools

**Skill version:** \`0.1.0\` — generated by \`scripts/build-llm-context.mjs\`. Do not edit this file directly; regenerate from the source.

---

`

const skillMd = readFileSync(join(skillDir, 'SKILL.md'), 'utf8')

const sections = [
  header,
  '# SKILL.md\n\n' + stripFrontmatter(skillMd),
]

for (const filename of orderedReferences) {
  const path = join(referencesDir, filename)
  const content = readFileSync(path, 'utf8')
  sections.push(`# references/${filename}\n\n` + stripFrontmatter(content))
}

// Detect any references not in orderedReferences (sanity check)
const actual = readdirSync(referencesDir).filter(f => f.endsWith('.md')).sort()
const expected = [ ...orderedReferences ].sort()
const missing = actual.filter(f => !expected.includes(f))
if (missing.length) {
  console.warn(`[build-llm-context] Warning: references/ contains files not in orderedReferences: ${missing.join(', ')}`)
}

const output = sections.join('\n\n---\n\n') + '\n'
writeFileSync(outputPath, output)

const lineCount = output.split('\n').length
console.log(`[build-llm-context] Wrote ${outputPath} (${lineCount} lines, ${output.length} chars)`)
